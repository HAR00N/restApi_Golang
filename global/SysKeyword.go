package SysKeyword

func SLASH() string {
	const slash string = "/"
	return slash
}

func SLASHS() string {
	const slashs string = "_"
	return slashs
}

func DOT() string {
	const dot string = "."
	return dot
}

func AND() string {
	const and string = "&"
	return and
}

func COMMON_UPDATE_NUMBER() int {
	const common_update_number int = 1
	return common_update_number
}
